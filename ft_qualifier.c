#include "ft_printf.h"

int		ft_qualifier(char s) //спецификаторы
{
	if (s == 's' || s == 'S'
		|| s == 'p' || s == 'd'
		|| s == 'P' || s == 'i'
		|| s == 'o' || s == 'O'
		|| s == 'u' || s == 'U'
		|| s == 'x' || s == 'X'
		|| s == 'c' || s == 'C')
		return (1);
	else
		return (0);
}
