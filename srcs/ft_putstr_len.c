#include "../includes/ft_printf.h"

int		ps_l(char const *str, int i)
{
	int j;

	j = 0;
	if (str[j] != '\0')
	{
		while (j < i)
		{
			ft_putchar(*str);
			j++;
		}
	}
	return (i);
}